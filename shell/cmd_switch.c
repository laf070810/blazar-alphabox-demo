/*******************************************************************************
 * Includes
 ******************************************************************************/
#include "Blazar_shell.h"
#include "Blazar_Switch.h"
 
 /*******************************************************************************
 * Definitions
 ******************************************************************************/


 /*******************************************************************************
 * Variables
 ******************************************************************************/
static bool Initialized = false;

 /*******************************************************************************
 * Prototypes
 ******************************************************************************/


 /*******************************************************************************
 * Code
 ******************************************************************************/
int32_t do_switch(p_shell_context_t context, int32_t argc, char **argv)
{
	if (argc == 1)
	{
		if (Initialized)
		{
			SW_Deinit();
			PRINTF("Switches are deinitialized. \r\n\r\n");
			Initialized = false;
		}
		else
		{
			SW_Init();
			PRINTF("Switches are initialized. \r\n\r\n");
			Initialized = true;
		}
		
		return 0;
	}
	
	PRINTF("Error: Unknown option. \r\n\r\n");
	return 1;
}
